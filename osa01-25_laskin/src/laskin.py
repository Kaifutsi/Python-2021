# Kirjoita ratkaisu tähän
Luku1 = int(input("Luku 1:"))
Luku2 = int(input("Luku 2:"))
kom = input("Komento:")

if kom == "summa":
    print(f"{Luku1} + {Luku2} = {Luku1 + Luku2}")
if kom == "erotus":
    print (f"{Luku1} - {Luku2} = {Luku1 - Luku2}")
if kom == "tulo":
    print(f"{Luku1} * {Luku2} = {Luku1 * Luku2}")